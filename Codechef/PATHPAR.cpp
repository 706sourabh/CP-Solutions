// This is a solution to PATH PARITY problem on CODECHEF
#include <iostream>
using namespace std;

int main() {
	// your code goes here
	int t, n, k;
	cin>>t;
	while(t--){
	    cin>>n>>k;
	    if(n%2!=0 && k==1){
	        cout<<"Yes"<<endl;
	    }
	    else if(n%2==0 && k==0 || n%2==0 && k==1){
	        cout<<"Yes"<<endl;
	    }
	    else if(n%2!=0 && k==0){
	        cout<<"No"<<endl;
	    }
	}
	return 0;
}
